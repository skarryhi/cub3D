#include "libft.h"

char		*ft_strdup(const char *str)
{
	char	*str2;
	int		i;

	if (!(str2 = (char*)malloc(sizeof(char) * ft_strlen(str) + 1)))
		return (NULL);
	i = 0;
	while (str[i])
	{
		str2[i] = str[i];
		i++;
	}
	str2[i] = '\0';
	return (str2);
}
